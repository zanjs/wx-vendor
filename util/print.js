export default {
  /**
   * 打印普通信息
   * @param {any} interfac
   */
  log(interfac) {
    console.log(interfac)
  },
  /**
   * 打印警告信息
   * @param {any} interfac
   */
  warn(interfac) {
    console.warn(interfac)
  },
  /**
   * 打印错误信息
   * @param {any} interfac
   */
  error(interfac) {
    console.error(interfac)
  },
}